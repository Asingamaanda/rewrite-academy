"""
[EDUCATIONAL, FRIENDLY TONE]

Let's start with natural numbers. These are the numbers we use for counting: 
one, two, three, four, and so on.

[PAUSE 0.5 seconds]

Natural numbers have special properties. When you add two natural numbers, 
you always get another natural number. For example, seven plus nine equals sixteen.

[PAUSE 0.5 seconds]

The same is true for multiplication. Six times eight equals forty-eight, 
which is also a natural number.

[PAUSE 0.5 seconds]

We say that natural numbers are "closed" under addition and multiplication.

[PAUSE 1 second]

However, natural numbers are NOT closed under subtraction. 
If we try to calculate five minus twenty, we can't get a natural number as the answer.

[PAUSE 1 second]
"""


# ----------------------------------------------------------------------------
# SCENE 4: WHOLE NUMBERS
# Duration: ~20 seconds
# ----------------------------------------------------------------------------

"""
[EXPLANATORY TONE]

Whole numbers are simply natural numbers plus zero.

[PAUSE 0.5 seconds]

Notice how zero is highlighted here. It's a very special number!

[PAUSE 1 second]

Zero is called the "identity element" for addition. When you add zero to any number, 
the number stays the same. Twenty-four plus zero equals twenty-four. 
One fifty-seven plus zero equals one fifty-seven.

[PAUSE 1 second]

Adding zero doesn't change the value!

[PAUSE 1 second]
"""


# ----------------------------------------------------------------------------
# SCENE 5: INTEGER NUMBERS
# Duration: ~35 seconds
# ----------------------------------------------------------------------------

"""
[ENGAGING TONE]

Now let's extend our number system to include integers. 
Integers are whole numbers PLUS negative numbers.

[PAUSE 1 second]

Look at the number line. Integers extend in both directions, 
from negative infinity on the left, through zero in the middle, 
to positive infinity on the right.

[PAUSE 1 second]

Now subtraction always works! Five minus eight equals negative three. 
One hundred minus one sixty-five equals negative sixty-five.

[PAUSE 1 second]

Integers also give us additive inverses. Twenty plus negative twenty equals zero. 
One thirty-five plus negative one thirty-five equals zero.

[PAUSE 1 second]

These pairs of numbers that add to zero are called additive inverses of each other.

[PAUSE 1 second]
"""


# ----------------------------------------------------------------------------
# SCENE 6: RATIONAL NUMBERS
# Duration: ~35 seconds
# ----------------------------------------------------------------------------

"""
[PRACTICAL, RELATABLE TONE]

Let's solve a real problem. Five people want to share twelve chocolate bars equally. 
How much does each person get?

[PAUSE 1 second]

Twelve divided by five doesn't give us a whole number. 
But we can express the answer as a fraction: twelve-fifths, 
or as a decimal: two point four.

[PAUSE 1 second]

This is where rational numbers come in. A rational number is any number 
that can be written as a fraction - one integer divided by another integer.

[PAUSE 0.5 seconds]

Three-quarters equals zero point seven five. 
Twenty-three tenths equals two point three.
Both fractions and decimals are rational numbers.

[PAUSE 2 seconds]
"""


# ----------------------------------------------------------------------------
# SCENE 7: IRRATIONAL NUMBERS
# Duration: ~30 seconds
# ----------------------------------------------------------------------------

"""
[INTRIGUED, CURIOUS TONE]

Here's an interesting question. What number, multiplied by itself, equals two?

[PAUSE 1 second]

One times one equals one - too small.
Two times two equals four - too big.
We can try decimal numbers, but we'll never find an exact fraction that works.

[PAUSE 1 second]

The answer is the square root of two, which is approximately one point four one four two one.
But this decimal goes on forever without repeating!

[PAUSE 0.5 seconds]

Numbers like this, that cannot be written as fractions, are called irrational numbers.

[PAUSE 0.5 seconds]

Other examples include the square root of three, the square root of five, 
and the famous number pi.

[PAUSE 2 seconds]
"""


