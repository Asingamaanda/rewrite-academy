# ----------------------------------------------------------------------------
# SCENE 14: LCM EXAMPLE
# Duration: ~30 seconds
# ----------------------------------------------------------------------------

"""
[ANALYTICAL TONE]

Let's find the Lowest Common Multiple, or LCM, of six and fifteen.

[PAUSE 1 second]

Multiples of six are: six, twelve, eighteen, twenty-four, thirty, and so on.
Multiples of fifteen are: fifteen, thirty, forty-five, sixty, and so on.

[PAUSE 1 second]

The common multiples are the numbers that appear in both lists: 
thirty, sixty, ninety.

[PAUSE 1 second]

The smallest common multiple is thirty. 
Therefore, the LCM of six and fifteen is thirty!

[PAUSE 2 seconds]
"""


# ----------------------------------------------------------------------------
# SCENE 15: HCF EXAMPLE
# Duration: ~40 seconds
# ----------------------------------------------------------------------------

"""
[METHODICAL TONE]

Now let's find the Highest Common Factor, or HCF, of two thousand three hundred and ten,
and three thousand five hundred and ten.

[PAUSE 1 second]

First, we break each number into prime factors.

[PAUSE 0.5 seconds]

Two thousand three hundred and ten equals two times three times five times seven times eleven.

[PAUSE 1 second]

Three thousand five hundred and ten equals two times three times three times five times thirteen.

[PAUSE 1 second]

The common prime factors are two, three, and five.

[PAUSE 1 second]

Multiply these together: two times three times five equals thirty.

[PAUSE 0.5 seconds]

The HCF is thirty!

[PAUSE 2 seconds]
"""


