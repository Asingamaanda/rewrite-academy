# ----------------------------------------------------------------------------
# SCENE 9: ESTIMATING & ROUNDING
# Duration: ~35 seconds
# ----------------------------------------------------------------------------

"""
[PRACTICAL, PROBLEM-SOLVING TONE]

A shop owner wants to buy chickens at thirty-eight rand each. 
With ten thousand rand, can he buy two hundred and fifty chickens?

[PAUSE 1 second]

Let's estimate by rounding. Thirty-eight rand rounds to forty rand.
Two hundred and fifty times forty equals ten thousand rand exactly!

[PAUSE 1 second]

So yes, he can buy about two hundred and fifty chickens, maybe even a bit more!

[PAUSE 1 second]

Now let's calculate exactly. Ten thousand divided by thirty-eight 
equals two hundred and sixty-three point one five.

[PAUSE 0.5 seconds]

He can buy two hundred and sixty-three chickens!

[PAUSE 1 second]

Estimation gives us a quick answer, and then we can calculate precisely if needed.

[PAUSE 2 seconds]
"""


# ----------------------------------------------------------------------------
# SCENE 10: COLUMN ADDITION
# Duration: ~40 seconds
# ----------------------------------------------------------------------------

"""
[METHODICAL, TEACHING TONE]

Let's learn how to add large numbers using columns. 
We'll calculate thirty-seven fifty-eight plus fifty-four eighty-six.

[PAUSE 1 second]

First, we write the numbers in columns, lining up the place values.

[PAUSE 0.5 seconds]

Start with the units column. Eight plus six equals fourteen. 
Write four in the units place, and carry one to the tens column.

[PAUSE 1 second]

In the tens column: five plus eight plus the carried one equals fourteen. 
Write four, carry one.

[PAUSE 1 second]

Hundreds: seven plus four plus one equals twelve. Write two, carry one.

[PAUSE 1 second]

Thousands: three plus five plus one equals nine.

[PAUSE 0.5 seconds]

The answer is nine thousand, two hundred and forty-four!

[PAUSE 2 seconds]
"""


# ----------------------------------------------------------------------------
# SCENE 11: COLUMN MULTIPLICATION
# Duration: ~35 seconds
# ----------------------------------------------------------------------------

"""
[STEP-BY-STEP INSTRUCTIONAL TONE]

Multiplying large numbers in columns is similar to addition. 
Let's multiply thirty-four eighty-nine by forty-seven.

[PAUSE 1 second]

First, multiply thirty-four eighty-nine by seven. This gives us twenty-four thousand, 
four hundred and twenty-three.

[PAUSE 1 second]

Next, multiply thirty-four eighty-nine by forty. 
This equals one hundred and thirty-nine thousand, five hundred and sixty.

[PAUSE 1 second]

Finally, add these two results together.

[PAUSE 1 second]

The final answer is one hundred and sixty-three thousand, nine hundred and eighty-three!

[PAUSE 2 seconds]
"""


# ----------------------------------------------------------------------------
# SCENE 12: LONG DIVISION
# Duration: ~50 seconds
# ----------------------------------------------------------------------------

"""
[PATIENT, CLEAR TONE]

Long division requires breaking the problem into manageable steps. 
Let's divide thirteen thousand, two hundred and fifty-four by fifty-six.

[PAUSE 1 second]

First estimate: How many fifty-sixes fit into thirteen thousand two hundred and fifty-four?
About two hundred. Two hundred times fifty-six equals eleven thousand, two hundred.

[PAUSE 1 second]

Subtract this from thirteen thousand two hundred and fifty-four. 
We get two thousand and fifty-four remaining.

[PAUSE 1 second]

Next estimate: Thirty times fifty-six equals one thousand, six hundred and eighty.
Subtract this to get three hundred and seventy-four.

[PAUSE 1 second]

Final estimate: Six times fifty-six equals three hundred and thirty-six.
We're left with a remainder of thirty-eight.

[PAUSE 1 second]

Add up our estimates: two hundred plus thirty plus six equals two hundred and thirty-six.

[PAUSE 0.5 seconds]

So thirteen thousand two hundred and fifty-four divided by fifty-six 
equals two hundred and thirty-six, with a remainder of thirty-eight.

[PAUSE 2 seconds]
"""


